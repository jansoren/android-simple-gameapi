package no.jts.android.simple.gameapi;

public class Setup {

	private int originDesignWidth;
	private int originDesignHeight;
	
	public Setup(int originDesignWidth, int originDesignHeight) {
		this.originDesignWidth = originDesignWidth;
		this.originDesignHeight = originDesignHeight;
	}

	public int getOriginDesignWidth() {
		return originDesignWidth;
	}

	public int getOriginDesignHeight() {
		return originDesignHeight;
	}
	
}
